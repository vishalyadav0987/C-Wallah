// Relation operator in C
#include<stdio.h>
int main()
{
    int a = 5;
    int b = 3;
    printf("a==b %d\n",a==b);  // 0==false
    printf("a!=b %d\n",a!=b);  // 1==true
    printf("a>=b %d\n",a>=b);  // 1
    printf("a<=b %d\n",a<=b);  // 0
    printf("a>b %d\n",a>b);    // 1
    printf("a<b %d\n",a<b);    // 0
    return 0;
    
}